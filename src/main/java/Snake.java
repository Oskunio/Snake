import javax.swing.*;

public interface Snake {
    int B_WIDTH = 300;
    int B_HEIGHT = 300;
    int ALL_DOTS = 900;
    int[] x = new int[ALL_DOTS];
    int[] y = new int[ALL_DOTS];
    int DOT_SIZE = 10;

    default void move(int dots, boolean leftDirection, boolean rightDirection, boolean upDirection, boolean downDirection) {
        for (int z = dots; z > 0; z--) {
            x[z] = x[(z - 1)];
            y[z] = y[(z - 1)];
        }

        if (leftDirection) {
            x[0] -= DOT_SIZE;
        }

        if (rightDirection) {
            x[0] += DOT_SIZE;
        }

        if (upDirection) {
            y[0] -= DOT_SIZE;
        }

        if (downDirection) {
            y[0] += DOT_SIZE;
        }
    }
    default void checkCollision(boolean inGame, int dots, Timer timer){

        for (int z = dots; z > 0; z--) {

            if ((z > 4) && (x[0] == x[z]) && (y[0] == y[z])) {
                inGame = false;
            }
        }

        if (y[0] >= B_HEIGHT) {
            inGame = false;
        }

        if (y[0] < 0) {
            inGame = false;
        }

        if (x[0] >= B_WIDTH) {
            inGame = false;
        }

        if (x[0] < 0) {
            inGame = false;
        }

        if (!inGame) {
            timer.stop();
        }
    }
}
